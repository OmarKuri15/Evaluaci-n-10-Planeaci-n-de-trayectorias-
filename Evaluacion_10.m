% 4. CONTROL, BUCLE DE SIMULACION %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
k = 1;          
idx_wp = 1;     
umbral_dist = 0.02; % Tolerancia de 2 cm 

t(1) = 0;
v = 0;
w = 0;
Error = 0;

while idx_wp <= num_puntos 
    
    % Distancia hacia el waypoint actual
    hxe(k) = hxd(idx_wp) - hx(k);
    hye(k) = hyd(idx_wp) - hy(k);
    Error(k) = sqrt(hxe(k)^2 + hye(k)^2);

    % Cambio de punto
    if Error(k) < umbral_dist
        idx_wp = idx_wp + 1;
        if idx_wp > num_puntos
            break; 
        end
        hxe(k) = hxd(idx_wp) - hx(k);
        hye(k) = hyd(idx_wp) - hy(k);
        Error(k) = sqrt(hxe(k)^2 + hye(k)^2);
    end

    % --- LEY DE CONTROL: GIRAR Y LUEGO AVANZAR ---
    theta_d = atan2(hye(k), hxe(k)); % Hacia dónde debo mirar
    
    e_theta = theta_d - phi(k); % Cuánto me falta girar
    e_theta = atan2(sin(e_theta), cos(e_theta)); % Normalizar ángulo
    
    Kw = 10.0; % Ganancia de giro
    Kv = 2.0;  % Ganancia de avance
    
    % Si el robot no está mirando directamente al punto (error > ~5 grados)
    if abs(e_theta) > 0.1 
        v_actual = 0; % SE DETIENE (no avanza)
        w_actual = Kw * e_theta; % SOLO GIRA
    else
        % Cuando ya lo está mirando, avanza directo hacia él
        v_actual = Kv * Error(k); 
        w_actual = Kw * e_theta; 
    end
    
    % Saturación
    vmax = 1.0; 
    wmax = 2.0; 
    if abs(v_actual) > vmax, v_actual = sign(v_actual)*vmax; end
    if abs(w_actual) > wmax, w_actual = sign(w_actual)*wmax; end

    v(k) = v_actual;   
    w(k) = w_actual;   
    % ---------------------------------------------

    % 5. APLICACIÓN DE CONTROL AL ROBOT %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
    phi(k+1) = phi(k) + w(k)*ts; 
            
    xp1 = v(k) * cos(phi(k)); 
    yp1 = v(k) * sin(phi(k));
 
    x1(k+1) = x1(k) + ts*xp1; 
    y1(k+1) = y1(k) + ts*yp1; 

    hx(k+1) = x1(k+1); 
    hy(k+1) = y1(k+1);
      
    t(k+1) = t(k) + ts;
    k = k + 1;
end
N_total = k-1; 

